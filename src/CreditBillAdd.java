    void CreateBill()
    {
         try
        {
            String sql="select bill_no from bill";
            
            pst=conn.prepareStatement(sql);

             rst=pst.executeQuery();
             int bid=50000;
             while(rst.next())
             {
                 bid=rst.getInt(1);
                 bid=bid+1;
             }
             
             jTextField1.setText(String.valueOf(bid));
             pst.close();
             rst.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
    
     public void InsertBill()
     {
         try
         {
               Statement st=conn.createStatement(); 
               String billno=jTextField1.getText();
               String Dates=jTextField2.getText();
               String cid=jTextField3.getText();
               int row=jTable1.getRowCount();
               
           for(int i=0;i<row;i++)
           {
               String pname=jTable1.getValueAt(i,0).toString();
               String qty=jTable1.getValueAt(i,1).toString();
               String mrp=jTable1.getValueAt(i,2).toString();
               String price=jTable1.getValueAt(i,3).toString();
              
               String sql="insert into bill values(?,?,?,?,?,?,?)";
               pst=conn.prepareStatement(sql);
               pst.setString(1,billno);
               pst.setString(2,pname);
               pst.setString(3,qty);
               pst.setString(4,mrp);
               pst.setString(5,price);
               pst.setString(6,Dates);
               pst.setString(7,cid);
               pst.execute();
               pst.close();
           }
           //JOptionPane.showMessageDialog(null,"Details has been added");
         }
         
         catch(Exception e)
         {
             JOptionPane.showMessageDialog(null,e);
         }
         
     }
     
     public void CalculateTotal()
     {
          int row=jTable1.getRowCount();
          float total=0;
               
           for(int i=0;i<row;i++)
           {
               String price=jTable1.getValueAt(i,3).toString();
               total=total+Float.parseFloat(price);
           }
           jTextField8.setText(String.valueOf(total));
     }
