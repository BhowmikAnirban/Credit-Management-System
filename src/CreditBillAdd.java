    public void search()
     {
         String u=jTextField3.getText();
         String sql="select * from customer where c_id='"+u+"'";
         try
         {
             pst=conn.prepareStatement(sql);
             rst=pst.executeQuery();
             if(rst.next())
             {
                 jTextField7.setText(rst.getString(2));
             }
             else
             {
                 JOptionPane.showMessageDialog(null,"Customer not found");
             }
             pst.close();
             rst.close();
         }
         catch(Exception e)
         {
             JOptionPane.showMessageDialog(null, e);
         }
         
     }
    
     void AddCredit()
     {
         try
        {
            String sql="insert into credit values(?,?,?,?)";
            
            pst=conn.prepareStatement(sql);
            
            pst.setString(1,jTextField3.getText());
            pst.setString(2,jTextField8.getText());
            pst.setString(3,jTextField2.getText());
            pst.setString(4,jTextField1.getText());
            pst.execute();
            
            JOptionPane.showMessageDialog(null,"Credit has been added for "+jTextField7.getText()+"");
           // JOptionPane.showMessageDialog(null,jTextField2.getText());
            pst.close();
            rst.close();
            setVisible(false);
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }                
     }
    
